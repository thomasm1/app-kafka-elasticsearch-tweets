DROP DATABASE IF EXISTS testdb;
DROP USER IF EXISTS `testadmin`@`%`;
CREATE DATABASE IF NOT EXISTS testdb CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
